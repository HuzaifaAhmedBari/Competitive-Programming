bool isprime(ll n)
{
    if(n<2)	return false;
    if(n==2 || n==3)	return true;
    if(n%2==0 || n%3==0)	return false;
    for(ll i=5;i*i<=n;i++)
    {
        if(n%i==0 || (n%(i+2))==0)
            return false;
    }
    return true;
}
